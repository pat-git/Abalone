package abalone.model;

/**
 * The enum Color represents the different colors of the balls in the game
 * abalone.
 * <p>
 * Colors:
 * <li>{@link #BLACK}</li>
 * <li>{@link #WHITE}</li>
 * <li>{@link #NONE}</li>
 * <p>
 * 
 * @version 1.0
 */
public enum Color {
    /**
     * Color black is used for a slot with a black ball.
     */
    BLACK,

    /**
     * Color white is used for a slot with a white ball.
     */
    WHITE,

    /**
     * Color none is used for a empty slot.
     */
    NONE
}
